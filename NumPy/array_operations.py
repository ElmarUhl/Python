import numpy as np

a = np.array([1,2,3])
print('Original array')
print(a)
print()
print(f'This is the sum of array elements: {a.sum()}')
print(f'This is the highest value of array elements: {a.max()}')
print(f'This is the lowest value of array elements: {a.min()}')
print(f'This is the mean value of array elements: {a.mean()}')
